    private void btnprontoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprontoActionPerformed
        
        if (tfdnome.getText().trim().isEmpty() || tfdnomeusuario.getText().trim().isEmpty()) {
            lblerror.setVisible(true);
        } else {
        
        FuncionarioDAO dao = new FuncionarioDAO();
        Funcionario funcionario = new Funcionario();
        funcionario = dao.consultarId(id);
        
        funcionario.setNome(tfdnome.getText());
        funcionario.setUsername(tfdnomeusuario.getText());
        funcionario.setEmail(tfdemail.getText());
        
        dao.atualizar(funcionario);
        JOptionPane.showMessageDialog(null, "Perfil editado com sucesso!");
        }
    }//GEN-LAST:event_btnprontoActionPerformed


